/**
 * This enum contains the max values
 * of each supported view mode in my project.
 *
 * There are 3 modes:
 *
 * @param Mobile - Min Value = 320
 *
 * @param Tablet - Min Value = 768
 *
 * @param PC_and_Above - Min Value = 1024
 */
export const enum ViewMode {
  Mobile = 320,
  Tablet = 768,
  PCAndLarger = 1024,
}
